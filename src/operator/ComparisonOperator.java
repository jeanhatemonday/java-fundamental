package operator;

public class ComparisonOperator {

    public static void main(String[] args) {
        /*
        Operator	Name	                    Example
        ==	        Equal to	                x == y
        !=	        Not equal	                x != y
        >	        Greater than	            x > y
        <	        Less than	                x < y
        >=	        Greater than or equal to	x >= y
        <=	        Less than or equal to	    x <= y
         */
        int x = 5;
        int y = 3;

        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x > y);
        System.out.println(x < y);
        System.out.println(x >= y);
        System.out.println(x <= y);
    }
}
