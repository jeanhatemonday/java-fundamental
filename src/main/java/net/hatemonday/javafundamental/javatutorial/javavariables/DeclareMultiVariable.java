package net.hatemonday.javafundamental.javatutorial.javavariables;

public class DeclareMultiVariable {

    public static void main(String[] args) {
        int x = 5, y = 6, z = 50;
        System.out.println(x + y + z);

        int a, b, c;
        a = b = c = 50;
        System.out.println(a + b + c);
    }
}
