package net.hatemonday.javafundamental.javatutorial.javastrings;

public class NumberAndString {

    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println(c);

        String d = "10";
        String e = "20";
        String f = d + e;
        System.out.println(f);

        String g = "10";
        int h = 20;
        String i = g + h;
        System.out.println(i);
    }
}
